	<p>
		The idea of fully separating Content, Design and Functionality has been sniffling in the corners for many years, but
		it has, by many, been considered an utopian ideal. It would be nice, but it just can't be done. Right?
	</p>
	<p>
		Wrong!
	</p>
	<p>
		Think of HTML as XML. Use it to structure your content semantically and forget about the design and functionality. 
		The design and functionality is likely to change, even before your current project is finished, and surely the
		design update scheduled next year will bring about even bigger changes. So leave it out of the equation and just think
		of content and semantics. CSS and JavaScript will take care of the rest.
	</p>

	<h3>Mobile first</h3>
	<p>
		Think mobile first when you structure your content - even if you are not building a mobile optimized website. Why? Because
		there simply isn't any reason not to. If you evade the mobile considerations, you degrade the accessibility of 
		your content. Thinking mobile forces you to forget the desktop design and focus on your actual content.
	</p>

