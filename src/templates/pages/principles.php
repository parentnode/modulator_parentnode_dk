	<h1>The principles <br />of Modulator</h1>

	<h2>Content, Design and Functionality</h2>
	<p>
		The idea of fully 
		<a href="http://en.wikipedia.org/wiki/Separation_of_presentation_and_content" target="_blank">separating Content, Design and Functionality</a>
		has been around for long. It has been considered anything between an absolute necessity to an utopian ideal. Supporters
		were fighting for semantic content and opponents for unconstrained design. It would be nice to be able to please both
		camps, but it is too complicated and probably just can't be done. Right? Wrong!
	</p>
	<p>
		Think of HTML as XML. Use it to structure your content semantically and forget about the design and functionality. 
		The design and functionality is likely to change, even before your current project is finished, and surely the
		design update scheduled next year will bring about even bigger changes. So leave it out of the equation and just think
		of content and semantics. CSS and JavaScript will take care of the rest.
	</p>
	<p>
		As a frontend programmer just think of your HTML as a flat content database. Query your data, manipulate and extend.
	</p>

	<h2>Mobile first</h2>
	<p>
		Think <a href="http://www.uxmatters.com/mt/archives/2012/03/mobile-first-what-does-it-mean.php">mobile first</a> 
		when you structure your content - even if you are not building a mobile optimized website. 
		Why? Because there simply isn't any reason not to. If you evade the mobile considerations, you degrade the 
		accessibility of your content. Thinking mobile first forces you to forget the desktop design and focus on your 
		actual content. Your markup will become simpler and more flexible.
	</p>

	<h2>Semantic HTML and SEO</h2>
	<p>
		Modulator implements <a href="http://schema.org" target="_blank">Schema.org</a> for extended semantics. Schema has
		been adapted by Google, Yahoo and Microsoft which makes it the broadest supported semantic model available. When
		using Modulator markup you automatically achieve the highest level of Search engine optimization from a code point
		of view, but you still need to bring your own content.
	</p>





	<h2>Additional levarage</h2>
	<p>
		<a href="http://detector.parentnode.dk" target="_blank">Detector</a> is an easy way of
		grouping all browsers into 9 simple target segments for faster and better interfaces.
	</p>
	<p>
		<a href="http://manipulator.parentnode.dk" target="_blank">Manipulator</a> is a full JavaScript library,
		designed to work with Modulator markup and Detector segments.
	</p>
	<p>
		<a href="http://janitor.parentnode.dk" target="_blank">Janitor</a> is a full PHP Content Management Framework and
		Content Delivery Platform designed to work with Modulator, Detector and Manipulator.
	</p>

